package com.coresecure.brightcove.wrapper.services;

public class BrightcoveReplicationException extends Exception {
    public BrightcoveReplicationException() {
        super();
    }

    public BrightcoveReplicationException(String message) {
        super(message);
    }

    public BrightcoveReplicationException(String message, Throwable cause) {
        super(message, cause);
    }

    public BrightcoveReplicationException(Throwable cause) {
        super(cause);
    }
}
